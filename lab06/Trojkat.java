package lab06;

public class Trojkat {
    double wys, podst;

    public Trojkat() {
        this.wys = 0;
        this.podst = 0;
    }

    public double getWys() {
        return wys;
    }

    public void setWys(double wys) {
        this.wys = wys;
    }

    public double getPodst() {
        return podst;
    }

    public void setPodst(double podst) {
        this.podst = podst;
    }
}
